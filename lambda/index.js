const Alexa = require('ask-sdk-core');

const LaunchRequestHandler = {
    
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'LaunchRequest';
    },
    handle(handlerInput) {
        const speakOutput = 'Hello, I am Pratyusha, the developer of this Alexa skill. If you want to know more about my education, tech stacks, interest or contact ask Alexa!';

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt('If you dont want to continue say stop to end the session')
            .getResponse();
    }
};



